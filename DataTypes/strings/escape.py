# Printing Escape Sequences
print("\nEscape sequences:")

# \n newlne
print("Newline: Hello\nWorld")

# \b give tab space between words
print("Tab: Hello\tWorld")

# Backslash: This is a backslash: \
print("Backslash: This is a backslash: \\")

# Single Quote: This is a single quote: '
print("Single Quote: This is a single quote: \'")

# Double Quote: This is a double quote: "
print("Double Quote: This is a double quote: \"")

# Backspace: HelloWorld
print("Backspace: Hello\bWorld")

# Carriage Return: \r
print("Carriage Return: Hello\rWorld")




