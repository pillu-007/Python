# Assignment
a = 5
print("a =", a)

# Addition assignment
a += 3
print("a += 3:", a)

# Subtraction assignment
a -= 2
print("a -= 2:", a)

# Multiplication assignment
a *= 4
print("a *= 4:", a)

# Division assignment
a /= 2
print("a /= 2:", a)

# Modulo assignment
a %= 3
print("a %= 3:", a)

# Exponentiation assignment
a **= 2
print("a **= 2:", a)

# Floor division assignment
a //= 3
print("a //= 3:", a)
